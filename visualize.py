    ax_b.set_ylabel('Participant', fontsize=12, fontweight='bold')
    ax_b.grid(axis='x', zorder=0); ax_b.set_axisbelow(True)
    for i, g in enumerate(gain[order]):
        ax_b.text(g + (0.15 if g>=0 else -0.15), i, f'{g:+.1f}',
                  va='center', ha='left' if g>=0 else 'right', fontsize=8.5, fontweight='bold')
    n_pos = int(np.sum(gain >= 0))
    ax_b.text(0.97, 0.04, f'Hybrid best in {n_pos}/15 folds\nMean advantage: {gain.mean():+.2f} pp',
              transform=ax_b.transAxes, ha='right', va='bottom', fontsize=9.5, fontweight='bold',
              bbox=dict(boxstyle='round', facecolor='#E8F8F5', edgecolor='#27AE60', alpha=0.9))

    for ax, l in zip([ax_a, ax_b], ['(a)','(b)']):
        ax.text(0.5, -0.16, l, transform=ax.transAxes, fontsize=15,
                fontweight='bold', ha='center', va='top')

    plt.tight_layout(); plt.subplots_adjust(bottom=0.16, wspace=0.28)
    _save('Figure07'); plt.close()


