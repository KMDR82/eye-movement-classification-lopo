# Figure 4 (Figure04.png): aggregated confusion matrix, two panels
# ============================================================================
def make_figure04():
    plt.rcParams.update({'figure.facecolor':'white','axes.facecolor':'white',
                         'savefig.facecolor':'white','font.family':'DejaVu Sans'})
    cm = load_confusion()
    classes = ['Fixation', 'Saccade', 'Smooth\nPursuit']
    fig, (ax_a, ax_b) = plt.subplots(1, 2, figsize=(15, 6.2))

    # (a) raw counts
    im_a = ax_a.imshow(cm, cmap='Blues', aspect='auto')
    ax_a.set_xticks(range(3)); ax_a.set_yticks(range(3))
    ax_a.set_xticklabels(classes, fontsize=11); ax_a.set_yticklabels(classes, fontsize=11)
    ax_a.set_xlabel('Predicted', fontsize=13, fontweight='bold')
    ax_a.set_ylabel('True', fontsize=13, fontweight='bold')
    thr = cm.max()/2
    for i in range(3):
        for j in range(3):
            ax_a.text(j, i, f'{cm[i,j]:,}', ha='center', va='center',
                      color='white' if cm[i,j] > thr else 'black', fontsize=13, fontweight='bold')
    plt.colorbar(im_a, ax=ax_a, fraction=0.046, pad=0.04).set_label('Count', fontsize=11)

    # (b) row-normalized recall
    cm_norm = cm.astype(float) / cm.sum(axis=1, keepdims=True) * 100
    im_b = ax_b.imshow(cm_norm, cmap='RdYlGn', aspect='auto', vmin=0, vmax=100)
    ax_b.set_xticks(range(3)); ax_b.set_yticks(range(3))
    ax_b.set_xticklabels(classes, fontsize=11); ax_b.set_yticklabels(classes, fontsize=11)
    ax_b.set_xlabel('Predicted', fontsize=13, fontweight='bold')
    ax_b.set_ylabel('True', fontsize=13, fontweight='bold')
    for i in range(3):
        for j in range(3):
            ax_b.text(j, i, f'{cm_norm[i,j]:.1f}%', ha='center', va='center',
                      color='black', fontsize=13, fontweight='bold')
    plt.colorbar(im_b, ax=ax_b, fraction=0.046, pad=0.04).set_label('Recall (%)', fontsize=11)

    for ax, letter in zip([ax_a, ax_b], ['(a)','(b)']):
        ax.text(0.5, -0.18, letter, transform=ax.transAxes, fontsize=15,
                fontweight='bold', ha='center', va='top')

    plt.tight_layout(); plt.subplots_adjust(bottom=0.16, wspace=0.30)
    _save('Figure04'); plt.close()


