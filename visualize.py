    ax_d.bar(xpos, fix, color=C_FIX, edgecolor='black', linewidth=0.5, label='Fixation', zorder=3)
    ax_d.bar(xpos, sp,  bottom=fix, color=C_SP, edgecolor='black', linewidth=0.5, label='Smooth Pursuit', zorder=3)
    ax_d.bar(xpos, sac, bottom=fix+sp, color=C_SAC, edgecolor='black', linewidth=0.5, label='Saccade', zorder=3)
    ax_d.set_xticks(xpos); ax_d.set_xticklabels(persons, rotation=45, fontsize=9)
    ax_d.set_ylabel('Sequences (stacked by class)', fontsize=12, fontweight='bold')
    ax_d.set_xlabel('Participant', fontsize=11)
