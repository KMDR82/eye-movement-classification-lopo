    parts = ax_a.violinplot(data, positions=[0,1,2], widths=0.7,
                            showmeans=False, showmedians=False, showextrema=False)
    for pc, c in zip(parts['bodies'], C):
        pc.set_facecolor(c); pc.set_alpha(0.35); pc.set_edgecolor(c); pc.set_linewidth(1.5)
    bp = ax_a.boxplot(data, positions=[0,1,2], widths=0.25, patch_artist=True, showfliers=True,
                      medianprops=dict(color='black', linewidth=2),
                      whiskerprops=dict(color='#333', linewidth=1.5),
                      capprops=dict(color='#333', linewidth=1.5),
                      flierprops=dict(marker='o', markerfacecolor='red', markersize=6, alpha=0.7))
    for patch, c in zip(bp['boxes'], C):
        patch.set_facecolor(c); patch.set_alpha(0.75); patch.set_edgecolor('black'); patch.set_linewidth(1.3)
    np.random.seed(1)
    for i, d in enumerate(data):
        ax_a.scatter(np.random.normal(i, 0.04, len(d)), d, s=22, color='black', alpha=0.45, zorder=5)
    ax_a.set_xticks([0,1,2]); ax_a.set_xticklabels(labels, fontsize=11)
    ax_a.set_ylabel('Per-fold Accuracy (%)', fontsize=12, fontweight='bold')
    ax_a.set_ylim(45, 98); ax_a.grid(axis='y', zorder=0); ax_a.set_axisbelow(True)
    for i, d in enumerate(data):
        ax_a.text(i, 47, f'SD={d.std(ddof=1):.2f}', ha='center', fontsize=9.5,
                  fontweight='bold', color=C[i])
    ax_a.annotate('p00', xy=(1, 51.89), xytext=(1.35, 54), fontsize=9, color=C[1],
                  fontweight='bold', arrowprops=dict(arrowstyle='->', color=C[1], lw=1.2))

    # (b) per-participant advantage of the hybrid
    best_single = np.maximum(cnn, lstm)
    gain = hyb - best_single
    order = np.argsort(gain)
    colors_g = ['#C0392B' if g < 0 else '#27AE60' for g in gain[order]]
    ypos = np.arange(len(persons))
    ax_b.barh(ypos, gain[order], color=colors_g, edgecolor='black', linewidth=1, alpha=0.85, zorder=3)
    ax_b.set_yticks(ypos); ax_b.set_yticklabels([persons[i] for i in order], fontsize=9.5)
