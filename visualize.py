#!/usr/bin/env python3
# ============================================================================
# Figure generation for the LOPO eye-movement classification study
# ============================================================================
# Produces the publication figures (300 DPI, MDPI style: white background,
# bottom panel labels, no super-title). File names follow the manuscript:
#
#   Figure01.png  - dataset overview            (paper Figure 1)
#   Figure02.png  - model architecture          (paper Figure 3)
#   Figure04.png  - confusion matrix            (paper Figure 4)
#   Figure05.png  - training dynamics           (paper Figure 5)
#   Figure06.png  - ablation study              (paper Figure 6)
#   Figure07.png  - per-fold distribution       (paper Figure 7)
#
# Note: the label-generation schematic (paper Figure 2, Figure03.png) was
# produced separately as a vector graphic and is not generated by this script.
#
# Data sources:
#   - Figure01 reads fig1_person_distribution.csv and fig1_sigma_distribution.csv
#   - Figure05 reads fig4_training_history.csv
#   These are produced by lopo_experiment.py / training_history.py (see RESULTS).
#   The confusion matrix and per-fold accuracies below are the values produced
#   by the 15-fold LOPO run (lopo_fold_results.csv).
# ============================================================================

import matplotlib
matplotlib.use('Agg')
import matplotlib.pyplot as plt
import matplotlib.patches as mpatches
from matplotlib.patches import FancyBboxPatch, FancyArrowPatch
import numpy as np
import pandas as pd
from pathlib import Path

# Directories. RESULTS holds the CSVs; OUT receives the figures.
RESULTS = Path('results')
OUT     = Path('figures')
OUT.mkdir(parents=True, exist_ok=True)

BASE_RC = {
    'figure.facecolor': 'white', 'axes.facecolor': 'white',
    'savefig.facecolor': 'white', 'axes.edgecolor': 'black',
    'axes.grid': True, 'grid.color': '#cccccc', 'grid.linewidth': 0.6,
    'grid.alpha': 0.5, 'font.family': 'DejaVu Sans',
}

# Class colors, consistent across figures.
C_FIX, C_SAC, C_SP = '#E67E22', '#C0392B', '#3498DB'
# Model colors, consistent across figures.
C_MODEL = ['#E67E22', '#3498DB', '#27AE60']  # CNN, LSTM, CNN+LSTM

# ----------------------------------------------------------------------------
# Data loaders. All figure values are read from the outputs of
# lopo_experiment.py rather than being hard-coded, so the figures always
# reflect the actual experiment results.
#   - lopo_fold_results.csv : per-fold accuracies (columns acc_CNN, acc_LSTM,
#                             acc_CNN+LSTM; one row per held-out participant)
#   - yt_all.npy, hyb_all.npy : pooled true labels and CNN+LSTM predictions
# ----------------------------------------------------------------------------
def load_fold_results():
    """Return (persons, acc_cnn, acc_lstm, acc_hyb) from lopo_fold_results.csv,
    ordered by participant id (p00..p14)."""
    df = pd.read_csv(RESULTS / 'lopo_fold_results.csv')
    df = df.sort_values('test_person').reset_index(drop=True)
    persons = df['test_person'].tolist()
    return (persons,
            df['acc_CNN'].values * 100.0,
            df['acc_LSTM'].values * 100.0,
            df['acc_CNN+LSTM'].values * 100.0)

def load_confusion():
    """Compute the aggregated CNN+LSTM confusion matrix from the pooled
    predictions saved by lopo_experiment.py (yt_all.npy, hyb_all.npy).
    Rows = true (Fixation, Saccade, Smooth Pursuit), columns = predicted."""
    from sklearn.metrics import confusion_matrix
    yt  = np.load(RESULTS / 'yt_all.npy')
    hyb = np.load(RESULTS / 'hyb_all.npy')
    return confusion_matrix(yt, hyb)

def _save(fig_stem, png_only=False):
    exts = [('png', dict(dpi=300))] if png_only else \
           [('png', dict(dpi=300)), ('pdf', dict()), ('tiff', dict(dpi=300))]
    for ext, kw in exts:
        path = OUT / f'{fig_stem}.{ext}'
        plt.savefig(path, bbox_inches='tight', facecolor='white', **kw)
        print(f'  saved {path}')


