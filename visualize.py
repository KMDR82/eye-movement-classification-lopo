# Figure 5 (Figure05.png): training dynamics for a representative fold (p11)
# ============================================================================
def make_figure05():
    plt.rcParams.update(BASE_RC)
    df = pd.read_csv(RESULTS / 'fig4_training_history.csv')
    ep = df['epoch'].values
    C_TRAIN, C_VAL = '#2E86C1', '#E67E22'
